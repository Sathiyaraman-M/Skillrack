N = int(input())
print(N * '*')
for i in range(1, N // 2):
    print('*', (i - 1) * '-', '*', (N - 2 * i - 2) * '-', '*', (i - 1) * '-','*', sep = '')
if N % 2 != 0:
    print('*', ((N // 2) - 1) * '-', '*', ((N // 2) - 1) * '-', '*', sep = '')
for i in range((N // 2) - 1, 0, -1):
    print('*', (i - 1) * '-', '*', (N - 2 * i - 2) * '-', '*', (i - 1) * '-', '*', sep = '')
print(N * '*')